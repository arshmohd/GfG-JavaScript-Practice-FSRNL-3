console.log("String to anything.");
const a = "25";
console.log(a);
console.log(typeof a);
console.log(+a);
console.log(typeof +a);
console.log(!!a);
console.log(typeof !!a);
console.log("-------------------------");
console.log("Number to anything.");
const b = 25;
console.log(b);
console.log(typeof b);
console.log("" + b);
console.log(typeof ("" + b));
console.log(!!b);
console.log(typeof !!b);
console.log("-------------------------");
console.log("Boolean to anything.");
const c = true;
const d = false;
console.log(c);
console.log(d);
console.log(typeof c);
console.log(typeof d);
console.log(+c);
console.log(+d);
console.log(typeof +c);
console.log(typeof +d);
console.log("" + c);
console.log("" + d);
console.log(typeof ("" + c));
console.log(typeof ("" + d));
